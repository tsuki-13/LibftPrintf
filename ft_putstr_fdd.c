#include "ft_printf.h"

void	ft_putstr_fdd(char *s, int fd, int *count)
{
	if (!s)
	{
		*count += write(1, "(null)", 6);
		return ;
	}
	while (*s)
	{
		*count += write(fd, s, 1);
		s++;
	}
}
